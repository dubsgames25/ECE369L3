# SAD (Sum of Absolute Differences) function ############################################################################################
.text
.globl SAD
SAD:
    # Allocate stack space and save registers
    addi $sp, $sp, -32
    sw $ra, 28($sp)
    sw $t0, 24($sp)
    sw $t1, 20($sp)
    sw $t2, 16($sp)
    sw $t3, 12($sp)
    sw $t4, 8($sp)
    sw $t5, 4($sp)
    sw $t6, 0($sp)

    # Load function arguments
    move $t0, $a0  # temp array pointer
    move $t1, $a1  # window array pointer
    move $t2, $a2  # size

    # Initialize sum and windowSum
    move $t3, $zero  # sum
    move $t4, $zero  # windowSum

    # Loop through the arrays and calculate the sums
loop:
    beq $t2, $zero, end_loop
    lw $t5, ($t0)
    lw $t6, ($t1)
    add $t3, $t3, $t5
    add $t4, $t4, $t6
    addi $t0, $t0, 4
    addi $t1, $t1, 4
    addi $t2, $t2, -1
    j loop

end_loop:
    # Calculate the absolute difference
    sub $t5, $t3, $t4
    abs $v0, $t5

    # Restore registers and return
    lw $t6, 0($sp)
    lw $t5, 4($sp)
    lw $t4, 8($sp)
    lw $t3, 12($sp)
    lw $t2, 16($sp)
    lw $t1, 20($sp)
    lw $t0, 24($sp)
    lw $ra, 28($sp)
    addi $sp, $sp, 32
    jr $ra

